package fr.epsi.api.profitability;

public class GrossRentability {

	private int mensualRent;
	private int buyPrice;
	
	public int getMensualRent() {
		return mensualRent;
	}
	public void setMensualRent(int mensualRent) {
		this.mensualRent = mensualRent;
	}
	public int getBuyPrice() {
		return buyPrice;
	}
	public void setBuyPrice(int buyPrice) {
		this.buyPrice = buyPrice;
	}
}
